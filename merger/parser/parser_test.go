// ---- helpers ----------------------------------------------------------------

func mustParse(t *testing.T, sql string) Statement {
	t.Helper()
	stmt, err := Parse(sql)
	if err != nil {
		t.Fatalf("Parse(%q) error: %v", sql, err)
	}
	return stmt
}

func ptr(s string) *string { return &s }

