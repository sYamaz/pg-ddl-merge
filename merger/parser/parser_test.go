func TestParseCreateTable_FKDetails(t *testing.T) {
	stmt := mustParse(t, `CREATE TABLE t (
		user_id int REFERENCES users(id) ON DELETE CASCADE ON UPDATE RESTRICT DEFERRABLE INITIALLY DEFERRED,
		CONSTRAINT fk_team FOREIGN KEY (user_id) REFERENCES teams(id) MATCH FULL ON DELETE SET NULL NOT DEFERRABLE
	)`)
	ct := stmt.(CreateTableStmt)

	// inline FK with ON DELETE / ON UPDATE / DEFERRABLE
	col := ct.Columns[0]
	if len(col.InlineConstraints) == 0 {
		t.Fatal("InlineConstraints should contain FK clause")
	}
	upper := strings.ToUpper(col.InlineConstraints[0])
	for _, kw := range []string{"ON DELETE CASCADE", "ON UPDATE RESTRICT", "DEFERRABLE", "INITIALLY DEFERRED"} {
		if !strings.Contains(upper, kw) {
			t.Errorf("missing %q in InlineConstraints: %s", kw, col.InlineConstraints[0])
		}
	}

	// named FK with MATCH FULL / ON DELETE / NOT DEFERRABLE
	if len(ct.Constraints) != 1 {
		t.Fatalf("expected 1 table constraint, got %d", len(ct.Constraints))
	}
	def := strings.ToUpper(ct.Constraints[0].Definition)
	for _, kw := range []string{"MATCH FULL", "ON DELETE SET NULL", "NOT DEFERRABLE"} {
		if !strings.Contains(def, kw) {
			t.Errorf("missing %q in constraint def: %s", kw, ct.Constraints[0].Definition)
		}
	}
}

